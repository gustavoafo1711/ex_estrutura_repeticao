"""
Escreva um program que escreva na tela de 1 até 20, de 1 em 1. Faça 2 vezes usando as estruturas de
repetição 'for' e 'while'.
"""
print("================ Contagem usando 'for' ================")
for i in range(1, 21):
    print(i)

print("\n================ Contagem usando 'while' ================")
x = 1
while x <= 20:
    print(x)
    x += 1

